#include <iostream>

using namespace std;


int main() {

	/* The "\n" is used here to separate sentences in each part of the program
	   for the sake of presentation but "endl" has been used to end lines that contain
	   words/sentences. */
	   
	   
	//Part a
	cout << "Tran says this is her first computer program." << endl;
	
	//Part b
	cout << "\nTran says this is her" << endl;
	cout << "first computer program." << endl;
	
	//Part c
	cout <<"\nTran says this is her" << endl << "first computer program." << endl;
	
	//Part d
	cout <<"\nTran says this is ";
	cout <<"her first computer program." << endl;
	
	//Part e
	cout << "\nTran said, \"This is my first computer program.\"" << endl;
	
	//Part f
	cout << "\nTran said, \"This is my" << endl;
	cout <<"first computer program.\"" << endl;
	
    //Part g
	cout <<"\nTran said, \"This is my" << endl << "first computer program.\"" << endl;
	
	//Part h
	cout << "\nTran said, \"This is ";
	cout << "my first computer program.\"" << endl;
	
	
	system("pause");
	return 0;
}


